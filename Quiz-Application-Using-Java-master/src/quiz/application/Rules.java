package quiz.application;

import javax.swing.*;
import java.awt.*;
import java.awt.event.*;

public class Rules extends JFrame implements ActionListener{

    String name;
    JButton start, back;
    
    Rules(String name) {
        this.name = name;
        getContentPane().setBackground(Color.WHITE);
        setLayout(null);
        
        JLabel heading = new JLabel("Welcome " + name + " to Quiz Master");
        heading.setBounds(50, 20, 700, 30);
        heading.setFont(new Font("Viner Hand ITC", Font.BOLD, 28));
        heading.setForeground(new Color(30, 144, 254));
        add(heading);
        
        JLabel rules = new JLabel();
        rules.setBounds(20, 90, 700, 350);
        rules.setFont(new Font("Tahoma", Font.PLAIN, 16));
        rules.setText(
            "<html>"+ 
                "1. The Quiz consist of 10 multiple-choice questions." + "<br><br>" +
                "2. You will have  15 seconds to answer each question. Once the timer runs out, your answer will be locked." + "<br><br>" +
                "3. You can use the 50-50 lifeline once during the Quiz. This lifeline will eliminate two incorrect options, leaving you with two choices to pick from." + "<br><br>" +
                "4. Each question has four options, and you must choose the correct one." + "<br><br>" +
                "5. The 50-50 lifeline can only be used on one question and cannot be reused." + "<br><br>" +
                "6. You cannot go back to previous questions or change your answers once submitted." + "<br><br>" +
                "7. There is no negative marking for incorrect answers." + "<br><br>" +
                "8. Make sure to focus and answer carefully, as the timer won't pause." + "<br><br>" +
            "<html>"
        );
        add(rules);
        
        back = new JButton("Back");
        back.setBounds(250, 500, 100, 30);
        back.setBackground(new Color(30, 144, 254));
        back.setForeground(Color.WHITE);
        back.addActionListener(this);
        add(back);
        
        start = new JButton("Start");
        start.setBounds(400, 500, 100, 30);
        start.setBackground(new Color(30, 144, 254));
        start.setForeground(Color.WHITE);
        start.addActionListener(this);
        add(start);
        
        setSize(800, 650);
        setLocation(350, 100);
        setVisible(true);
    }
    
    public void actionPerformed(ActionEvent ae) {
        if (ae.getSource() == start) {
            setVisible(false);
            new Quiz(name);
        } else {
            setVisible(false);
            new Login();
        }
    }
    
    public static void main(String[] args) {
        new Rules("User");
    }
}
